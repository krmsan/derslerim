public class C01_DataCasting {
    public static void main(String[] args) {
        int sayi1=20;
        int sayi2=6;
        System.out.println(sayi1/sayi2);// 3 olur tamsayı kısmını alır
        System.out.println(sayi1/(double)sayi2);// 3.3333333333

        double sayi3=6;
        System.out.println(sayi1/sayi3);//3.3333...
        //- Kullanicidan iki double sayi alin, ilk sayiyi ikinci sayiya bolun ve bolum
        //isleminin sonucununun tamsayi kismini yazdirin.



    }
}
