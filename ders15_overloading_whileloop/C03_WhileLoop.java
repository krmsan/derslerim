package ders15_overloading_whileloop;

public class C03_WhileLoop {
    public static void main(String[] args) {

        // 2 basamaklı sayıları boslukla yaz
        for (int i = 10; i <=99 ; i++) {
            System.out.println(i+"");
        }
        System.out.println("");

        // while ile
        int a =10;
        while (a<100){
            System.out.println(a+" ");
            a++;

        }

    }
}
